#include <cstdio>

int main() {

    printf("1 > 2  : %d\n", 1 > 2);
    printf("2 > 1  : %d\n", 2 > 1);
    printf("1 < 2  : %d\n", 1 < 2);
    printf("2 < 1  : %d\n", 2 < 1);
    printf("1 == 1 : %d\n", 1 == 1);
    printf("2 == 1 : %d\n", 2 == 1);
    printf("1 >= 2 : %d\n", 1 >= 2);
    printf("1 <= 1 : %d\n", 1 <= 1);
    printf("1 != 1 : %d\n", 1 != 1);
    printf("1 != 2 : %d\n", 1 != 2);
}