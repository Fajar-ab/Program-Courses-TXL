#include <cstdio>

int main() {

    printf("2 > 1                            : %d\n", 2 > 1);
    printf("!(2 > 1)                         : %d\n", !(2 > 1));
    printf("(2 > 1) && (3 > 1)               : %d\n", (2 > 1) && (3 > 1));
    printf("((2 > 1) || (3 < 1)) && (1 == 1) : %d\n", ((2 > 1) || (3 < 1)) && (1 == 1));
    printf("(1 != 1) ^ !(1 != 1)             : %d\n", (1 != 1) ^ !(1 != 1));
}